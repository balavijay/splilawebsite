                    <p>The next day Srila Prabhupada's health suddenly became much worse, and he couldn't come down to the temple. This was the first crisis since his coming to England, and suddenly his plans changed. Instead of going on to the United States as he had planned, he now requested that he be taken back to India. He spoke of Bombay. "If I live a few days more," he said, "let me see the opening of the Bombay temple. We can wait here and then fly to Bombay. I have worked so hard for it. If I see the opening and then die, it will be a very peaceful death. Even if I live, I can come back here."

