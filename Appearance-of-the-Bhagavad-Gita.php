                    <h1>50 Years Ago - Did Srila Prabhupada receive the first copy of the Bhagavad-gita today?</h1>
                    <p>The story of the Bhagavad-gita As It Is has its beginnings as early as 1947. Twenty years, many struggles and near-misses later, Srila Prabhupada did finish his manuscript in 1967—and not for the first time. Srila Prabhupada asks Brahmananda Prabhu to print the manuscript. Brahmananda Prabhu is successful in his assigned mission and gets the Bhagavad-gita As It Is published possibly towards the end of 1968. This article attempts to fix a date on this event.</p>
                    <br> <br>


                    <b>Brahmananda and the Bhagavad-gita</b>
                    <p>The book Swamiji which talks of Brahmananda Prabhu’s experiences with Srila Prabhupada mentions that he received the copies in the fall of 1968. Srila Prabhupada was in Seattle at that time and Brahmananda Prabhu in New York. After Seattle, Prabhupada travels to Los Angeles. In all probability, Srila Prabhupada receives the copies of the Bhagavad-gita As It Is in Los Angeles. This can be concluded from the following excerpt from Radha Damodara Vilasa  I:</p>
                    <p>The second major step forward is in book publication. In mid-December a case of books arrives from New York. At the evening class, a devotees hands Prabhupada his newly published Bhagavad-gita As It Is.  Holding it up for everyone to see, Prabhupada is pleased that ISKCON finally has its own Gita. </p>
                    <p>So we can understand that the books arrived when Srila Prabhupada was giving a Bhagavad-gita class in the evening. If we focus on the tapes of classes given in December 1968, we come across an interesting find:</p>

                    <br> <br>
                    <p>Prabhupada: <i> Oh. All right. Where is that book, Bhagavad-gita? Oh, you must have the book in your hand, everyone. [laughter] </i></p>
                    <p>Sudama: <i>Our copies came today. </i></p>
                    <p>Prabhupada: <i>Which copy? Ah...</i></p>
                    <p>Sudama: <i>Each of us will have a copy now.</i></p>
                    <p>Prabhupada: <i>Back to Godhead?</i></p>
                    <p>Sudama: <i>Bhagavad-gita.</i></p>
                    <p>Prabhupada: <i>Ah, Bhagavad-gita. That's all right. So everyone, you must possess a copy. </i></p>
                    (Lecture on Bhagavad-gita 2.27–38 -- December 11, 1968, Los Angeles)</i></p>

<br> <br>
                    <p>From this we can reasonably conclude the day that the books arrived to be December 11, 1968, which incidentally is mid-December. We spoke to Dayananda Prabhu, the then president of the Los Angeles temple, who could not remember the exact date, but he felt this may be the date.</p>
                    <p> </p><p> </p>

<br> <br>
                    <p><b>Earlier references</b></p>

                    <p>But doubts still remain, as to whether this was the day the books arrived first. Two letters written by Prabhupada on December 1 state that the Bhagavad-gita As It Is has already been published by this time:</p>
                    <p>Regarding the money which you shall soon attain, I think that the book fund may use it very well as we have so many new expenditures due to our new books, Bhagavad-gita As It Is (already published) and Teachings of Lord Caitanya (to be published next month).
                    <br><i>(Letter to: Jayapataka  --  Los Angeles 1 December, 1968)</i></p>
                   
                    <br> 
                    <p>As you may have heard, our Bhagavad-gita As It Is is now published and can be ordered from the New York temple by writing to Brahmananda.
                    <br> <i>(Letter to: Gurudasa  --  Los Angeles 1 December, 1968)</i></p> 
                    <br> 

                    <p>By December 9, the Bhagavad-gita As It Is is available in London:</p>
                    <p>I understand that the MacMillan Company is distributing our book, Bhagavad-gita As It Is, in London also, so if you can also retail this book to friends at the meeting it may prove very helpful in deepening their interest in this science of Krishna Consciousness.
                    <br><i>(Letter to: Gurudasa  --  Los Angeles 9 December, 1968)</i></p>
                    <br> 

                    <p>Going further back, we find a very important letter, the earliest that speaks of Srila Prabhupada having copies of the Bhagavad-gita As It Is with him. This is from November 25:</p>
                    <p>Our Bhagavad-gita as it is, is now published, and I have got copies of it here.
                    <br><i>(Letter to: Krsnadasa  --  Los Angeles 25 November, 1968)</i></p>
                    <br> <br>

              
                    <p><b>The actual date</b></p>
                    <p>Generally, Srila Prabhupada would mention any important event—like the purchase of some property or his meeting some important personality—in his letters. So we can safely assume he received the books a few days prior to this. If we search the audio transcripts, we find the earliest lecture from the Bhagavad–gita that was given during this visit to Los Angeles is on 23 November, 1968. In the lecture, a devotee reads out from the Introduction to the Bhagavad-gita, which confirms that the copy that Prabhupada used that day was from his own Bhagavad-gita As It Is. </p>
                    <p>Prabhupada: Somebody come read Bhagavad-gita. You will read? Come on. Our publication by Macmillan Company, Bhagavad-gita, is out.</p>
                    <p>(Lecture on Introduction to Bhagavad-gita As It Is—Los Angeles, November 23, 1968)</p>

                    <p>This is the earliest date-reference that we could find, and although we cannot say exactly that Srila Prabhupada received the Bhagavad-gita As It Is on this day, we can say that there is a high possibility—from the proof of the November 25 letter and this lecture. It can be that the copies arrived on 23 November, 1968 and Prabhupada immediately gave a class on that day, reading from his translation. Further during the Bhagavad-gita classes that were given after this one, devotees read from the Bhagavad-gita As It Is. This can be confirmed by going through the lecture transcripts.</p>

                   

