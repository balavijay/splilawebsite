                    <p class="abtUsP2"> Mukunda wanted to know more about the festival itself, so he spent a day in the library reading up about it. He found out that it had been held for thousands of years and that it was India’s largest outdoor festival. Over a million people attended every year. Even though the San Francisco’s Ratha Yatra would be tiny in comparison, it was exciting enough for it was America’s first-ever Ratha Yatra.</p>
					<p class="abtUsP2">At the end of June, Srila Prabhupada went to the hospital for a checkup. The doctor was surprised at Srila Prabhupada's recovery and had no objection to his flying to San Francisco. So in search of sunny skies, and eager to guide his followers in performing the first Ratha-Yatra, Prabhupada flew to San Francisco, New Jagannatha Puri.</p>
					<p class="abtUsP2"> At the San Francisco airport Prabhupada smiled but said little as the devotees greeted him with flowers and kirtana. Jayananda was waiting with his station wagon to drive Prabhupada to the private house they had rented north of the city, at Stinson Beach. But first, Prabhupada said, he wanted to visit the San Francisco Radha-Krishna temple. Jayananda drove to 518 Frederick Street. Prabhupada got out of the car and entered the small storefront, which was filled with waiting devotees and guests. He bowed before the smiling Jagannatha deities and, without speaking a word, left the room, returned to the car, and departed for Stinson Beach.</p>
					<p class="abtUsP2"> The house at Stinson Beach, called Paradisio, had a large living room overlooking the Pacific Ocean was decorated with some of Jadurani's paintings of Lord Vishnu, Radha and Krishna, and Lord Caitanya, as well as with Indian prints of Jagannatha Puri. Prabhupada's bedroom, also facing the ocean, had sliding windows. On the wall was a portrait of Srila Bhaktisiddhanta Sarasvati and a painting of Radha and Krishna. Prabhupada smiled and said the paintings were very nice. As he spent the days recuperating from his illness, he was also eagerly anticipating for the historic event to come… The Western world’s first Ratha Yatra!</p>
