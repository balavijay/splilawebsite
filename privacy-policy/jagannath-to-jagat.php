                    <p class="abtUsP2"> The storefront at Haight-Ashbury was no longer a mere storefront but had become something worthy: New Jagannatha Puri. Srila Bhaktisiddhanta Sarasvati had asked him to come here. Who could imagine how crazy these American hippies were! So many girls and boys-unhappy, mad, despite their wealth and education. But now, through Krishna consciousness, some were finding happiness.</p>
					<p class="abtUsP2"> Now the devotees were a family. If they followed his instructions and were sincere, Krishna would help them. They would be purified by chanting Hare Krishna and daily worship of the Deities. Lord Jagannatha was present, and the devotees would have to worship Him faithfully.</p>
					<p class="abtUsP2"> At the airport the devotees were crying. But Srila Prabhupada assured them he would return if they would hold a Ratha-Yatra festival. "You must arrange a procession down the main street," he told them. "Do it nicely. We must attract many people. They have such a procession yearly in Jagannatha Puri. At this time the Deity may leave the temple." Repeatedly he promised them he would return and asked them to cooperate among themselves.</p>
