                    <p class="abtUsP1"> “You all please maintain the center nicely. I will return to San Francisco for Ratha Yatra in July.” Srila Prabhupada had said at the airport when he departed to New York. Devotees maintained the daily routine of morning and evening programs, Sunday Love Feast and the park chanting. All the while they were eagerly expecting his return in July.</p>
                    <p class="abtUsP2"> Then, on June 1st, they received a phone call from New York informing that Srila Prabhupada had suffered a stroke and was in critical condition in Beth Israel Hospital in Manhattan. Doctors told devotees to be prepared that he might not live through the night.</p>
					<p class="abtUsP2"> Srila Prabhupada had given a special prayer to the devotees to chant all night and pray to Lord Nrisimha-deva, thinking, “Our master has not finished his work. Please protect him.”</p>
					<p class="abtUsP2"> There was a tremendous love and anxiety to have Srila Prabhupada recover from the crisis. Devotees kept the temple open all night, turned on the altar lights behind Jagannatha, lit candles, and chanted solemnly. News quickly spread down Haight Street, and soon the temple was crowded with people who came to chant through the night.</p>
					<p class="abtUsP2"> Next day an airmail arrived from Srila Prabhupada: </p>
					<p class="abtUsP2"> “My dear boys and girls, I am so much obliged to you for your prayers to Krishna to save my life. Due to your sincere and ardent prayer, Krishna has saved my life. I was to die on Tuesday certainly but because you prayed sincerely I am saved. Now I am improving gradually and coming to original condition. As soon as I get a little strength for traveling I shall come to San Francisco. In the meantime I shall be very glad to know what arrangements you are going to do for the Ratha Yatra festival. Make it a grand procession and unique introduction in the United States. Most probably I will be able to come to S.F. by the 25th of this month. Thanking you once more, Your ever well-wisher, A.C. Bhaktivedanta Swami.”</p>
					<p class="abtUsP2"> Srila Prabhupada was completely absorbed in serving Lord Jagannatha, and His Ratha Yatra so soon after almost dying. This letter gave devotees all renewed enthusiasm for organizing the Ratha Yatra, which they had all but forgotten in the turmoil since Srila Prabhupada’s stroke.</p>
