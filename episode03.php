                    <p>One morning Srila Prabhupada asked to go and see the Deities, and his men promptly moved him in his rocking chair before Krishna-Balarama. Sitting in his chair beneath the tamala tree, Prabhupada looked up at the transcendental brothers, while many tears glided down his cheeks. "They are dressed very nicely," he said. While he basked in the presence of Krishna and Balarama, he enjoyed the soothing shade of the tamala tree. "The contractor wanted this tree cut down," he said, "but I would not allow. There are not many tamala trees left. These worldly men do not know."</p>
                    <p>During the course of the monsoon months Srila Prabhupada met with his disciples from the Bhaktivedanta Institute and guided them in arranging for an international conference of scientists in Vrindavana titled, "Life Comes From Life." He also inspected the gurukula building. Meeting with his disciple Ambarisa Dasa, the great-grandson of Henry Ford, Prabhupada discussed the model for a Vedic planetarium based on the Fifth Canto of Srimad Bhagavatam. "I am afraid of a big conspiracy," he noted while receiving reports about the Mayapur temple being attacked by a gang of three hundred dacoits. </p>
                    <p>The local newspapers were giving reports on Srila Prabhupada's health, and a genuine concern for his well-being prevailed throughout Vrindavana and surrounding villages. Many arrived to have darshana of Srila Prabhupada.</p>
