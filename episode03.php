                    <p>At Srila Prabhupada's request, kirtanas by a group of no more than four or five devotees and Bhagavatam readings went on constantly, whether he was in his room, on the veranda, or on the roof. In the morning from five to ten and in the evenings from three to nine he would sit with his eyes closed, absorbed in the kirtana.</p>
